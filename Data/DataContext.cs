using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading;
using System.Threading.Tasks;
using Entities;
using Infrastructure;
using Microsoft.EntityFrameworkCore;

namespace Data
{
    public class DataContext : DbContext
    {
        private readonly IPersonAccessor _iPersonAccessor;
        private readonly AuditDataContext _auditDataContext;
        public DataContext(DbContextOptions<DataContext> options, IPersonAccessor iPersonAccessor, AuditDataContext auditDataContext) : base(options)
        {
            _auditDataContext = auditDataContext;
            _iPersonAccessor = iPersonAccessor;

        }
        public override async Task<int> SaveChangesAsync(bool acceptAllChangesOnSuccess, CancellationToken cancellationToken = default(CancellationToken))
        {
            SetInsertUpdateFields();
            var auditEntries = OnBeforeSaveChanges();
            var result = await base.SaveChangesAsync(acceptAllChangesOnSuccess, cancellationToken);
            await OnAfterSaveChanges(auditEntries);
            return result;
        }

        public override int SaveChanges()
        {
            //throw new Exception("user async verision of save changes.");
            return base.SaveChanges();
        }
        private async Task OnAfterSaveChanges(List<AuditEntryDto> auditEntries)
        {
            if (auditEntries == null || auditEntries.Count == 0)
                return;

            foreach (var auditEntry in auditEntries)
            {

                foreach (var prop in auditEntry.TemporaryProperties)
                {
                    if (prop.Metadata.IsPrimaryKey())
                    {
                        auditEntry.KeyValues[prop.Metadata.Name] = prop.CurrentValue;
                    }
                    else
                    {
                        auditEntry.NewValues[prop.Metadata.Name] = prop.CurrentValue;
                    }
                }


                await _auditDataContext.Audit.AddAsync(auditEntry.ToAudit());
                await _auditDataContext.SaveChangesAsync();
            }

            return;
        }

        private List<AuditEntryDto> OnBeforeSaveChanges()
        {
            if (_iPersonAccessor.IsPersonAuthenticated())
            {
                var personId = _iPersonAccessor.GetPersonId().ToString();
                ChangeTracker.DetectChanges();
                var auditEntries = new List<AuditEntryDto>();
                foreach (var entry in ChangeTracker.Entries())
                {
                    if (entry.Entity is Audit || entry.State == EntityState.Detached || entry.State == EntityState.Unchanged)
                        continue;
                    var auditEntry = new AuditEntryDto(entry);
                    auditEntry.TableName = entry.Entity.GetType().Name;
                    auditEntry.PersonId = personId;
                    auditEntries.Add(auditEntry);
                    foreach (var property in entry.Properties)
                    {
                        if (property.IsTemporary)
                        {
                            // value will be generated by the database, get the value after saving
                            auditEntry.TemporaryProperties.Add(property);
                            continue;
                        }
                        string propertyName = property.Metadata.Name;

                        if (property.Metadata.IsPrimaryKey())
                        {
                            auditEntry.KeyValues[propertyName] = property.CurrentValue;
                            continue;
                        }
                        switch (entry.State)
                        {
                            case EntityState.Added:
                                auditEntry.AuditTypeEnum = AuditTypeEnum.Create;
                                auditEntry.NewValues[propertyName] = property.CurrentValue;
                                break;
                            case EntityState.Deleted:
                                auditEntry.AuditTypeEnum = AuditTypeEnum.Delete;
                                auditEntry.OldValues[propertyName] = property.OriginalValue;
                                break;
                            case EntityState.Modified:
                                if (property.IsModified)
                                {
                                    auditEntry.ChangedColumns.Add(propertyName);
                                    auditEntry.AuditTypeEnum = AuditTypeEnum.Update;
                                    auditEntry.OldValues[propertyName] = property.OriginalValue;
                                    auditEntry.NewValues[propertyName] = property.CurrentValue;
                                }
                                break;
                        }
                    }
                }
                foreach (var auditEntry in auditEntries.Where(_ => !_.HasTemporaryProperties))
                {
                    _auditDataContext.Audit.Add(auditEntry.ToAudit());
                }
                return auditEntries.Where(_ => _.HasTemporaryProperties).ToList();
            }
            return null;
        }

        private void SetInsertUpdateFields()
        {
            if (_iPersonAccessor.IsPersonAuthenticated())
            {
                var currentDateTime = DateTimeOffset.UtcNow;
                var personId = _iPersonAccessor.GetPersonId();
                foreach (var item in ChangeTracker.Entries().Where(x => x.State == EntityState.Added && x.Entity is BaseEntity))
                {
                    var entity = item.Entity as BaseEntity;
                    entity.InsertDate = currentDateTime;
                    entity.InsertPersonId = personId;
                    entity.UpdateDate = currentDateTime;
                    entity.UpdatePersonId = personId;
                }

                foreach (var item in ChangeTracker.Entries().Where(x => x.State == EntityState.Modified && x.Entity is BaseEntity))
                {
                    var entity = item.Entity as BaseEntity;
                    entity.UpdateDate = currentDateTime;
                    entity.UpdatePersonId = personId;
                    item.Property(nameof(entity.InsertDate)).IsModified = false;
                    item.Property(nameof(entity.InsertPersonId)).IsModified = false;
                }
            }


        }

        public DbSet<Menu> Menu { get; set; }
        public DbSet<MenuPermission> MenuPermission { get; set; }
        public DbSet<ListItem> ListItem { get; set; }
        public DbSet<ListItemCategory> ListItemCategory { get; set; }
        public DbSet<Person> Person { get; set; }
        public DbSet<Project> Project { get; set; }

        public DbSet<ProjectMember> ProjectMember { get; set; }  
        public DbSet<ProjectModule> ProjectModule { get; set; }
        public DbSet<ProjectModuleDeveloper> ProjectModuleDeveloper { get; set; } 
        public DbSet<TestCaseDetail> TestCaseDetail { get; set; }
        public DbSet<TestCaseStepDetail> TestCaseStepDetail { get; set; } 
        public DbSet<PersonPersonalizationSetting> PersonPersonalizationSetting { get; set; }
        public DbSet<TestPlan> TestPlan { get; set; }
        public DbSet<TestPlanTestCase> TestPlanTestCase { get; set; }

        public DbSet<TestRun> TestRun { get; set; }
        public DbSet<TestRunPlan> TestRunPlan { get; set; }
        public DbSet<TestRunPlanDetail> TestRunPlanDetail { get; set; }
        public DbSet<TestRunHistory> TestRunHistory { get; set; }
        public DbSet<TestRunTestCaseHistory> TestRunTestCaseHistory { get; set; }
        public DbSet<TestRunTestCaseStepHistory> TestRunTestCaseStepHistory { get; set; }
        public DbSet<Entities.Environment> Environment { get; set; }
        public DbSet<Document> Document { get; set; }
        public DbSet<TestRunTestCaseHistoryDocument> TestRunTestCaseHistoryDocument { get; set; }

        public DbSet<ProjectStarred> ProjectStarred { get; set; }
        public DbSet<TestRunTestCaseStepHistoryDocument> TestRunTestCaseStepHistoryDocument { get; set; }
        protected override void OnModelCreating(ModelBuilder modelBuilder)
        {
            modelBuilder.HasPostgresExtension("citext");
            modelBuilder.Entity<Project>().Property(b => b.ProjectName)
                   .HasColumnType("citext");
        }

    }
}